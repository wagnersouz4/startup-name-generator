import UIKit

extension UIColor {

    /// Default background color used by the application
    open class var background: UIColor {
        return UIColor(colorLiteralRed: 108/255, green: 115/255, blue: 128/255, alpha: 1)
    }

    /// LightGreen
    open class var brightGreen: UIColor {
        return UIColor(colorLiteralRed: 155/255, green: 222/255, blue: 57/255, alpha: 1)
    }

}
